// Helper function to get unique categories from products
export const getCategories = (): string[] => {
  return Array.from(new Set(products.map((product) => product.category)));
};

// Helper function to get unique brands from products
export const getBrands = (): string[] => {
  return Array.from(new Set(products.map((product) => product.brand)));
};

// Helper function to get price range
export const getPriceRange = (): [number, number] => {
  const prices = products.map((product) => product.price);
  return [Math.min(...prices), Math.max(...prices)];
};
